using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace FloatVeriTipi
{
    internal class Program
    {
        static void Main(string[] args)
        {
            float birinci = 12.45f;
            // default olarak ondalıklı sayıları double veri tipinde alır sayı sonunda f işareti ile float türünü seçeriz
            float ikinci = -859.65f;

            Console.WriteLine(birinci);
            Console.WriteLine(ikinci);
            Console.ReadLine();
        }
    }
}
